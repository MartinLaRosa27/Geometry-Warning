#include "Enemigo.h"

// ------------------------------------------------------------------------------------------------------------------------
Enemigo::Enemigo(int velocidad)
{
    this->velocidad = velocidad;
}

// ------------------------------------------------------------------------------------------------------------------------
void Enemigo::incrementarVelocidad()
{
    this->velocidad--;
}

// ------------------------------------------------------------------------------------------------------------------------
int Enemigo::getVelocidad()
{
    return this->velocidad;
}

// ------------------------------------------------------------------------------------------------------------------------
Sprite* Enemigo::getSpriteEnemigo()
{
    return this->spriteEnemigo;
}
